const baseMetrics = (sent, deliveredPct, openPct, clickPct, bouncePct) => {
  const delivered = Math.round(sent * (deliveredPct / 100));
  const opened = Math.round(delivered * (openPct / 100));
  const clicked = Math.round(delivered * (clickPct / 100));
  const bounced = Math.round(sent * (bouncePct / 100));
  const unsubscribed = Math.round(delivered * 0.005);
  return {
    sent,
    delivered,
    opened,
    clicked,
    bounced,
    unsubscribed,
    openRate: delivered ? +((opened / delivered) * 100).toFixed(1) : 0,
    clickRate: delivered ? +((clicked / delivered) * 100).toFixed(1) : 0,
    bounceRate: sent ? +((bounced / sent) * 100).toFixed(2) : 0,
  };
};

