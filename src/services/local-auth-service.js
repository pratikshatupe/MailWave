/**
 * local-auth-service.js
 *
 * Frontend-only auth store. Persists locally-registered users in
 * `mailwave_users`, tracks the active session in `mailwave_current_user`,
 * and creates a matching tenant via local-tenant-service.
 *
 * NOTE: This is local demo only. Real production password validation must
 * be handled by the backend with hashing (bcrypt / argon2). Do not use
 * this code path for real user accounts.
 */

import { ROLES, ROLE_LABELS } from '../config/roles.js';
import { findDemoUser, findDemoUserByEmail } from '../config/demoUsers.js';
import { getPlanName } from '../config/plans.js';
import { ERROR_MESSAGES } from '../config/messages.js';
import {
  createTenantForUser,
  getTenants,
  getTenantById,
} from './local-tenant-service.js';

const USERS_KEY = 'mailwave_users';
const CURRENT_USER_KEY = 'mailwave_current_user';

function safeStorage() {
  try {
    return typeof window !== 'undefined' ? window.localStorage : null;
  } catch {
    return null;
  }
}

function readJson(key, fallback) {
  const storage = safeStorage();
  if (!storage) return fallback;
  try {
    const raw = storage.getItem(key);
    if (!raw) return fallback;
    const parsed = JSON.parse(raw);
    return parsed ?? fallback;
  } catch {
    return fallback;
  }
}

function writeJson(key, value) {
  const storage = safeStorage();
  if (!storage) return;
  try {
    if (value === null || value === undefined) storage.removeItem(key);
    else storage.setItem(key, JSON.stringify(value));
  } catch {
    /* quota — ignored in demo */
  }
}

function normaliseEmail(emailId) {
  return String(emailId || '').trim().toLowerCase();
}

function generateUserId(emailId) {
  const slug = normaliseEmail(emailId).split('@')[0].replace(/[^a-z0-9]+/gi, '-') || 'user';
  return `user-${slug}-${Date.now().toString(36)}`;
}

function deriveRole(accountType) {
  if (accountType === 'organisation' || accountType === 'organization') {
    return ROLES.BUSINESS_ADMIN;
  }
  return ROLES.INDIVIDUAL;
}

/* ------------------------------ Users CRUD ------------------------------ */

export function getUsers() {
  const list = readJson(USERS_KEY, []);
  return Array.isArray(list) ? list : [];
}

export function saveUsers(users) {
  writeJson(USERS_KEY, Array.isArray(users) ? users : []);
}

export function isEmailAlreadyRegistered(emailId) {
  const target = normaliseEmail(emailId);
  if (!target) return false;
  if (findDemoUserByEmail(target)) return true;
  return getUsers().some((u) => normaliseEmail(u.emailId) === target);
}

/* ----------------------------- Registration ----------------------------- */

/**
 * Persist a new user + tenant locally. Throws { code: 'EMAIL_EXISTS' } if
 * the Email ID is already taken (demo OR locally registered).
 *
 * Payload shape:
 *   { fullName, emailId, password, contactNumber, accountType, selectedPlan }
 *
 * `password` is stored as `passwordHashOrDemoPassword`. This is local demo
 * only. Real production password validation must be handled by backend with
 * hashing.
 */
export function registerUser(payload) {
  const emailId = normaliseEmail(payload.emailId);
  if (!emailId) {
    const err = new Error('Email ID is required.');
    err.code = 'INVALID_PAYLOAD';
    throw err;
  }
  if (isEmailAlreadyRegistered(emailId)) {
    const err = new Error(ERROR_MESSAGES.emailAlreadyRegistered);
    err.code = 'EMAIL_EXISTS';
    throw err;
  }

  const accountType =
    payload.accountType === 'organization' ? 'organisation' : payload.accountType;
  const role = deriveRole(accountType);
  const userId = generateUserId(emailId);
  const tenantId = null; // generated by tenant service for stable shape

  const fullName = String(payload.fullName || '').trim();
  const user = {
    id: userId,
    tenantId,
    fullName,
    emailId,
    // Demo-only password. Real backend must hash this.
    passwordHashOrDemoPassword: payload.password,
    contactNumber: payload.contactNumber || '',
    accountType,
    role,
    selectedPlan: payload.selectedPlan || 'starter',
    selectedPlanName: getPlanName(payload.selectedPlan || 'starter'),
    emailVerified: false,
    status: 'Active',
    createdAt: new Date().toISOString(),
  };

  // Create the tenant first so we can stamp tenantId onto the user record.
  const tenant = createTenantForUser({
    ...user,
    organisationName: payload.organisationName,
  });
  user.tenantId = tenant.tenantId;
  user.organisationName = tenant.organisationName;

  const users = getUsers();
  users.push(user);
  saveUsers(users);

  return { user, tenant };
}

/* -------------------------------- Login -------------------------------- */

/**
 * Verify credentials against locally-registered users first, then fall back
 * to the demo user table. Returns the persisted current-user record or null.
 */
export function loginUser(emailId, password) {
  const target = normaliseEmail(emailId);
  if (!target || !password) return null;

  const localUser = getUsers().find(
    (u) => normaliseEmail(u.emailId) === target && u.passwordHashOrDemoPassword === password
  );
  if (localUser) {
    const current = toCurrentUser(localUser);
    setCurrentUser(current);
    return current;
  }

  const demo = findDemoUser(target, password);
  if (demo) {
    const current = toCurrentUserFromDemo(demo);
    setCurrentUser(current);
    return current;
  }
  return null;
}

export function logoutUser() {
  writeJson(CURRENT_USER_KEY, null);
}

export function getCurrentUser() {
  return readJson(CURRENT_USER_KEY, null);
}

export function setCurrentUser(currentUser) {
  if (!currentUser) {
    writeJson(CURRENT_USER_KEY, null);
    return null;
  }
  writeJson(CURRENT_USER_KEY, currentUser);
  return currentUser;
}

/* ---------------------------- Shape helpers ---------------------------- */

function toCurrentUser(user) {
  return {
    id: user.id,
    tenantId: user.tenantId,
    fullName: user.fullName,
    emailId: user.emailId,
    role: user.role,
    accountType: user.accountType,
    selectedPlan: user.selectedPlan,
    selectedPlanName: user.selectedPlanName || getPlanName(user.selectedPlan),
    contactNumber: user.contactNumber || '',
    isAuthenticated: true,
  };
}

function toCurrentUserFromDemo(demo) {
  return {
    id: demo.email,
    tenantId: demo.tenantId,
    fullName: demo.name,
    emailId: demo.email,
    role: demo.role,
    accountType:
      demo.role === ROLES.SUPER_ADMIN
        ? 'platform'
        : demo.role === ROLES.INDIVIDUAL
        ? 'individual'
        : 'organisation',
    selectedPlan: demo.plan?.toLowerCase?.() || 'starter',
    selectedPlanName: demo.plan || 'Starter',
    contactNumber: '',
    isAuthenticated: true,
  };
}

/* ----------------------------- Super Admin ----------------------------- */

/**
 * Return every registered user — both locally registered AND the
 * built-in demo users — in a uniform shape for the Super Admin Users page.
 */
export function getUsersForSuperAdmin() {
  const locallyRegistered = getUsers().map((u) => ({
    id: u.id,
    fullName: u.fullName,
    emailId: u.emailId,
    contactNumber: u.contactNumber || '—',
    role: u.role,
    roleLabel: ROLE_LABELS[u.role] || 'User',
    accountType:
      u.accountType === 'organisation' || u.accountType === 'organization'
        ? 'Organisation'
        : 'Individual',
    selectedPlan: u.selectedPlanName || getPlanName(u.selectedPlan),
    tenantId: u.tenantId,
    status: u.status || 'Active',
    createdAt: u.createdAt,
    source: 'local',
  }));

  return locallyRegistered;
}

/**
 * Return every tenant for the Super Admin Tenants page.
 */
export function getTenantsForSuperAdmin() {
  const tenants = getTenants();
  return tenants.map((t) => ({
    tenantId: t.tenantId,
    organisationName: t.organisationName,
    ownerName: t.ownerFullName || '',
    ownerEmailId: t.ownerEmailId || '',
    accountType:
      t.accountType === 'organisation' || t.accountType === 'organization'
        ? 'Organisation'
        : 'Individual',
    selectedPlan: getPlanName(t.selectedPlan),
    status: t.status || 'Active',
    createdAt: t.createdAt,
  }));
}

